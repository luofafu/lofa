# 算术运算符 % // **
# 求余 % 是否是3的倍数=0
print(3000 % 3)
# 取整 //
print(3001.121 / 2)
print(3001.121 // 2)
# 取幂 **
print(2**4)
# 赋值运算符 =  += -= /= *=
number = 5000
number += 1
number -= 1
print(number)
# 关系运算符 > < >= <= ==
money1 = 'a'
money2 = 'b'
print(money1 > money2)
# 逻辑运算符 and or not
print(True and False)
print(True or True)
print(not(True) or True)

